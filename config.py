# config.py

# Этот файл теперь содержит только самые базовые константы,
# которые нужны до того, как бот сможет подключиться к базе данных.
# Все остальные настройки были перенесены в БД и управляются через app_config.py

import os
from dotenv import load_dotenv

# Рекомендуется для хранения секретов, таких как токен бота.
# Бот будет пытаться загрузить их из .env файла, но если их там нет,
# он будет использовать значения из базы данных, которые можно задать через админку.
load_dotenv()

# Путь к базе данных SQLite. Это единственная настройка, которая должна быть здесь.
DATABASE_NAME = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'vpn_bot.db')